    private void reload()
    {
        airlinerTextField.setText(flight.getAirplane().getFleet().getAirliners().getAirlines());
        flightIdTextField.setText(String.valueOf(flight.getFlightId()));
        DefaultTableModel dtm = (DefaultTableModel) seatsTable.getModel();
        dtm.setRowCount(0);
        for(Seat s: flight.getListOfSeats() )
        {
            if(!(s.isBooked()))
            {
                Object row[]=new Object[2];
                row[0]=s;
                row[1]=flight.getPriceOfSeat();
                
                dtm.addRow(row);
            }
        }
        dtm = (DefaultTableModel) custTable.getModel();
        dtm.setRowCount(0);
        for(Person p:travelAgency.getCustomerDirectory().getListOfPerson())
        {
            Object row[] = new Object[2];
            row[0]=p;
            row[1]=p.getName();
          
            dtm.addRow(row);
        }
        
    }

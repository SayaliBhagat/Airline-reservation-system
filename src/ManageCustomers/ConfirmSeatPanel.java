    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:
        if(nameTextField.getText().isEmpty())
            JOptionPane.showMessageDialog(null, "Please enter the name before searching", "Error", 0);
        else
        {
            DefaultTableModel dtm = (DefaultTableModel) custTable.getModel();
            dtm.setRowCount(0);
            for(Person p:travelAgency.getCustomerDirectory().getListOfPerson())
            {
                if((p.getName()).toLowerCase().matches((nameTextField.getText()).toLowerCase()))
                {
                    Object row[]=new Object[2];
                    row[0]=p;
                    row[1]=p.getName();
                    dtm.addRow(row);
                }
            }
        }
    }//GEN-LAST:event_searchButtonActionPerformed

    private void viewDetailsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDetailsButtonActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = custTable.getSelectedRow();
        
        
        if (selectedRow < 0 )
            JOptionPane.showMessageDialog(null, "Please select a row from Customer Table");
        else
        {
            addCustomerButton.setEnabled(false);
            custIdTextField.setEnabled(false);
            custNameTextField.setEnabled(false);
            emailIdTextField.setEnabled(false);
            Person p = (Person) custTable.getValueAt(selectedRow, 0);
            
            custIdTextField.setText(String.valueOf(p.getPersonId()));
            custNameTextField.setText(p.getName());
            emailIdTextField.setText(p.getEmailId());
            addCustomerButton.setEnabled(false);
            
            
            ViewJPanel vPanel = new ViewJPanel(travelAgency, flight, cardSequenceJPanel,p);
            cardSequenceJPanel.add("ViewJPanel",vPanel);
            CardLayout layout = (CardLayout)cardSequenceJPanel.getLayout();
            layout.next(cardSequenceJPanel);
        }
    }//GEN-LAST:event_viewDetailsButtonActionPerformed

    private void addCustomerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCustomerButtonActionPerformed
        // TODO add your handling code here:
        if((custIdTextField.getText().isEmpty()) || (custNameTextField.getText().isEmpty()) || (emailIdTextField.getText().isEmpty()))
        {
            JOptionPane.showMessageDialog(null, "Please enter Customer name,id and email-id", "Error", 0);
        }
        else
        {
            try
            {
                int id = Integer.parseInt(custIdTextField.getText());
                Person person= travelAgency.getCustomerDirectory().addPerson();
                person.setEmailId(emailIdTextField.getText());
                person.setName(custNameTextField.getText());
                person.setPersonId(id);
                reload();
                custIdTextField.setText("");
                custNameTextField.setText("");
                emailIdTextField.setText("");
                addCustomerButton.setEnabled(false);
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, "Please enter numeric data in customer id", "Error", 0);
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_addCustomerButtonActionPerformed

    private void bookButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = custTable.getSelectedRow();
        int selectRow1= seatsTable.getSelectedRow();
        if ((selectedRow < 0 ) || (selectRow1 < 0))
            JOptionPane.showMessageDialog(null, "Please select a customer from Customer Table and a seat from Seats Table for Booking the seat");
        else
        { 
            Person p = (Person) custTable.getValueAt(selectedRow, 0);
            Seat s = (Seat) seatsTable.getValueAt(selectRow1, 0);
            s.setPerson(p);
            s.setBooked(true);
            p.addSeat(s);
            custIdTextField.setText("");
            custNameTextField.setText("");
            emailIdTextField.setText("");
            addCustomerButton.setEnabled(true);
            JOptionPane.showMessageDialog(null, "Seat Booked Successfully");
            reload();
        }
    }//GEN-LAST:event_bookButtonActionPerformed


